package cidc.pqr.ws_Bizagi_obj;

public class ListasAtributosDatos {
	private String idAtributo;
	private String codigoAtributo;
	private String nombreAtributo;
	
	
	public String getIdAtributo() {
		return idAtributo;
	}
	public void setIdAtributo(String tipodePersonaId) {
		this.idAtributo = tipodePersonaId;
	}
	public String getCodigoAtributo() {
		return codigoAtributo;
	}
	public void setCodigoAtributo(String tipodePersonaCodigo) {
		this.codigoAtributo = tipodePersonaCodigo;
	}
	public String getNombreAtributo() {
		return nombreAtributo;
	}
	public void setNombreAtributo(String tipodePersonaNombre) {
		this.nombreAtributo = tipodePersonaNombre;
	}

}
